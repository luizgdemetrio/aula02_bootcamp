# 4. Faça um programa que peça dois números inteiros e imprima a divisão inteira do primeiro pelo segundo.

numero1 = int(input("Insira o primeiro numero inteiro: "))
numero2 = int(input("Insira o segundo numero inteiro: "))

divisao = numero1 // numero2

print(f"O resultado da divisao por inteiros e: {divisao}")

